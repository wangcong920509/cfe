package com.cfe.response.entity;

//20151108-getAllTask中地点信息类
public class PositionInfo {

	private double x;
	private double y;
	private String desc;
	
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	
	
}
